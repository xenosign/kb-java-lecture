package variable;

public class Var1 {
    public static void main(String[] args) {
        int a = 7;
        double b = 4.3;
        boolean c = true;
        char d = 'c';   // 반드시 단 따옴표 ' 사용, 한 글자만 가능!
        String e = "Hello, Java world!";    // 반드시 쌍 따옴표 " 사용, 여러 글자 가능!

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
